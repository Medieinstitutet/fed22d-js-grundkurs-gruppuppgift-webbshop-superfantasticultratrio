
function click1(click) {
    const quantity1 = document.getElementById("quantity1");
    const sumvalue = parseInt(quantity1.innerText) + click;
    quantity1.innerText = sumvalue;
  
    if(sumvalue <0) {
      quantity1.innerText = 0;
    }
  
    if(click === 0) {
      quantity1.innerText = 0;
    }
  }
  
